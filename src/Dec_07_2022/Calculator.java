package Dec_07_2022;

public class Calculator {

    public float add(float a, float b) {
        float c = a + b;
        return c;
    }

    public float sub(float a, float b) {
        float c = a - b;
        return c;
    }

    public float mul(float a, float b) {
        float c = a * b;
        return c;
    }

    public float divide(float a, float b) {
        float c = a / b;
        return c;
    }
}
