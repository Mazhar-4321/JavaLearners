package Dec_07_2022;

public class Calculator {

    int add(int a, int b) {
        int c = a + b;
        return c;
    }

    int sub(int a, int b) {
        int c = a - b;
        return c;
    }

    int mul(int a, int b) {
        int c = a * b;
        return c;
    }

    int divide(int a, int b) {
        int c = a / b;
        return c;
    }
}
