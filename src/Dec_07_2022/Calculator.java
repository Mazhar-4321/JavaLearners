package Dec_07_2022;

public class Calculator {

    float add(float a, float b) {
        float c = a + b;
        return c;
    }

    float sub(float a, float b) {
        float c = a - b;
        return c;
    }

    float mul(float a, float b) {
        float c = a * b;
        return c;
    }

    float divide(float a, float b) {
        float c = a / b;
        return c;
    }
}
